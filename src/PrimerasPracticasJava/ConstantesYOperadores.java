package PrimerasPracticasJava;
/*
 /* PARA DECLARAR LOS CONSTANTE SE PONE LA PALABRA FINAL ANTES DE LAS VARIABLES
 * EJ: final Integer EDAD;
 */

/*
 * OPERADORES ARITMETICOS
 *  + : SUMA
 *  - : RESTA
 *  * : MULTIPLICACION
 *  / : DIVISION
 */
/*
 * OPERADORES LOGICOS Y BOOLEANOS
 * > : MAYOR QUE
 * < : MENOR QUE
 * <>:MAYOR QUE Y MENOR QUE
 * !=:DISTINTO QUE
 * ==:INGUAL QUE
 * &&:Y LOGICO
 * O : O LOGICO
 */
/*
 * OPERADOR DE INCREMENTO Y DECREMENTO
 * ++ : INCREMENTO
 * -- : DECREMENTO
 * +=NUMERO QUE DESEAS INCREMENTAR EJ: +=3
 * -=NUMERO QUE DESEAS DECREMENTAR EJ: -=3
 */


//EN CASO DE DIVISION LOS TIPOS DE DATOS QUE SE TIENE QUE UTILIZAR ES DOUBLE O FLOAT
public class ConstantesYOperadores {
	public static void main(String[] args)
	{
		int a,b,c;
		a=5;
		b=7;
		c=a+b;
		
		//INCREMENTAR
		c++;
		System.out.println("El resultado es "+c);
	}
}
